    private void BpalavraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BpalavraActionPerformed
    palavra = JOptionPane.showInputDialog("Digite uma palavra: ");     
    dica = JOptionPane.showInputDialog("Digite uma dica:" );   
    Bpalavra.setEnabled(false);
    jButton1.setEnabled(true);
    btReset.setEnabled(true);
    
    //retira acento
    palavra = Normalizer.normalize(palavra, Normalizer.Form.NFD);
    palavra = palavra.replaceAll("[^\\p{ASCII}]" , "");

    //converter para maiusculo
    palavra = palavra.toUpperCase();

    //troca letra por traço
    LblPalavra.setText(palavra.replaceAll("[A-Z]", " _ "));
    
    }//GEN-LAST:event_BpalavraActionPerformed
