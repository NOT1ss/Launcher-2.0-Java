    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    letra = JOptionPane.showInputDialog("Digite uma letra: ");
    //Faz a letra ficar em maiúsculo
    letra = letra.toUpperCase();
    
    //Coloca a palavra certa em seu lugar específico e se for a errada adiciona +1 no erro
    if(palavra.contains(letra)){
        letraCerta = letraCerta + letra;
        LblPalavra.setText(palavra.replaceAll("[^"+letraCerta+"]", " _ "));
    }else{
        erro++;
    }
    
    //Verifica se a uma letra já foi digitada mais de uma vez e verifica os erros
    if(LblErradas.getText().contains(letra)){
        JOptionPane.showMessageDialog(null, "Letra já digitada!");
    }else{
        LblErradas.setText(LblErradas.getText()+letra+", ");
    }
    //Verifica se ganhou ou não
    if(palavra.equals(LblPalavra.getText())){
        JOptionPane.showMessageDialog(null, "Você Ganhou!!" + "\nA palavra era: " + palavra );
        Resetar();
    }  
    
    if(erro == 2){
        LblDica.setText("Dica: "+ dica); 
    }else if(erro == 6){
        JOptionPane.showMessageDialog(null, "Você Perdeu!!" + "\nA palavra era: " + palavra);
        Resetar();
    }

    if(erro == 1){
    cabeca.setEnabled(true);
    }
    else if(erro == 2){
    corpo.setEnabled(true);
    }
    else if(erro == 3){
    bracoE.setEnabled(true);
    }
    else if(erro == 4){
    bracoD.setEnabled(true);
    }
    else if(erro == 5){
    pernaE.setEnabled(true);
    }
    else if(erro == 6){
    pernaD.setEnabled(true);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

